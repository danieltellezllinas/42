int		ft_str_is_uppercase(char *str)
{
	int i;
	int a;

	a = 1;
	i = 0;
	while (str[i])
	{
		if (!(str[i] >= 'A' && str[i] <= 'Z'))
		{
			a = 0;
		}
		i++;
	}
	return (a);
}
